
let percentage = 85

switch(true){
    case percentage  >= 90:
        console.log("A+")
        break;
    case percentage  >= 80:
        console.log("A")
        break;
    case percentage  >= 70:
        console.log("B")
        break;
    case percentage  >= 60:
        console.log("C")
        break;
        
        default:
            console.log("FAIL")
}

